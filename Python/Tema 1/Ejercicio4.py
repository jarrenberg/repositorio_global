# 4. Escribe un programa que recoja tres números y calcule su media aritmética.
num1 = int(input("Introduzca un numero:"))
num2 = int(input("Introduzca otro numero:"))
num3= int(input("Introduzca otro numero:"))

media = float((num1+num2+num3)/3)
print("La media de los numeros introducidos es " + str(media))