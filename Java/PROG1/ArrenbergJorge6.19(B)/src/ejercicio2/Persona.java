package ejercicio2;

public class Persona {
	
	String dni;
	String nombre;
	String apellido;
	byte edad;
	
	public Persona(String dni, String nombre, String apellidos, byte edad){
	this.dni = dni;
	this.nombre = nombre;
	this.apellido = apellidos;
	this.edad = edad;
	}

}
